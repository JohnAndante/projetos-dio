package exemplos;

public class VI_PalavrasReservadas {
    /*
     ? Palavras Reservadas
     *
     * abstract     continue    goto        package     syncronized
     * assert       default     if          private     this
     * boolean      do          implements  protected   throw
     * break        double      import      public      throws
     * byte         else        instanceof  return      transient
     * case         extends     int         short       try
     * catch        final       interface   static      void
     * char         finally     long        strictfp    volatil
     * class        float       native      super       while
     * const        for         new         switch
     *
     */

     /*
      ? Controle de pacotes
      * import  -   importa pacotes ou classes para dentro do código
      * package -   especifica a que pacote todas as classes de um arquivo pertencem
      -
      ? Modificadores de Acesso
      * public      -   acesso de qualquer classe
      * private     -   acesso apenas por dentro da classe
      * protected   -   acesso por classes no mesmo pacote e subclasses
      -
      ? Primitivos
      * boolean -   valor indicando verdadeiro ou falso
      * byte    -   inteiro de 8 bits (signed)
      * char    -   caracter unicode (16-bit unsigned)
      * double  -   número de ponto flutuante de 64 bits (signed)
      * float   -   número de ponto flutuante de 32 bits (signed)
      * int     -   inteiro de 32 bits (signed)
      * long    -   inteiro de 64 bits (signed)
      * short   -   inteiro de 32 bits (signed)
      * void    -   sem retorno de valor
      -
      ? Modificadores de classes, variáveis ou métodos
      * abstract    -   classe que não pode ser instanciada ou método
      *                 que precisa ser implementado por uma subclasse não abstrata
      * class       -   especifica uma classe
      * extends     -   indica a superclasse que a subclasse está estendendo
      * final       -   impossibilita que uma classe seja estendida, que um método
      *                 seja sobrescrito ou que uma variável sera reinicializada
      * implements  -   indica as interfaces que uma classe irá implementar
      * interface   -   especifica uma interface
      * native      -   indica que um método está escrito em uma linguagem dependente
      *                 de plataforma, como o C
      * new         -   instancia novo objeto, chamando seu construtor
      * static      -   faz um método ou variável pertencer à classe ao invés de às instâncias
      * strictfp    -   usado em frente a um método ou classe para indicar que os
      *                 números de ponto flutuantes seguirão as regras de ponto
      *                 flutuante em todas as expressões
      * synchronized-   indica que um método só pode ser acessado por uma thread de cada vez
      * transient   -   impede a serialização de campos
      * volatile    -   indica que uma variável pode ser alterada durante o uso de threads
      -
      ? Controle de fluxo dentro de um bloco de código
      * break       -   sai do bloco de código em que ele está
      * case        -   executa um bloco de código dependendo do teste do switch
      * continue    -   pula a execução do código que viria após essa linha e vai para
      *                 a próxima passagem do loop
      -
      ? Tratamento de erros
      * assert  -   testa uma expressão condicional para verificar uma suposição do programador
      * catch   -   declara o bloco de código usado para tratar uma exceção
      * finally -   bloco de código após um try-catch que é executado independentemente do
      *             fluxo de programa seguido ao lidar com uma exceção
      * throw   -   usado para passar uma exceção para o método que o chamou
      * throws  -   indica que um método pode passar uma exceção para o método que o chamou
      * try     -   bloco de código que tentará ser executado, mas que pode causar uma exceção
      -
      ? Variáveis de referência
      * super   -   refere-se a superclasse imediata
      * this    -   refere-se a instância atual do objeto

      */

}
